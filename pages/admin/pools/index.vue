    showAddModal.value = !showAddModal.value;
}
const handleAddPool = async () => {
    //TODO : form validation 
    isLoading.value = true;
    await $client.pool.addPool.mutate({name : poolInfo.value.name});
    poolInfo.value.name = '';
    await fetchPools();
    isLoading.value =false;
    showAddModal.value = false;
}
const toggleEditModal = () => {
    showEditModal.value = !showEditModal.value;
}

const EditModal = async (poolId : string, poolName : string) => {

    poolInfo.value.id = poolId;
    poolInfo.value.name = poolName;
    showEditModal.value = !showEditModal.value;
   
}
const handleEditPool = async () => {
    //TODO : form validation 
    isLoading.value = true;
    await $client.pool.updatePool.mutate(poolInfo.value);
    poolInfo.value.id = '';
    poolInfo.value.name = '';
    await fetchPools();
    isLoading.value = false;
    showEditModal.value = false;
}
const toggleDeleteModal = () => {
    showDeleteModal.value = !showDeleteModal.value;
}

const DeleteModal = async (poolId: string, poolName: string) => {

    poolInfo.value.id = poolId;
    poolInfo.value.name = poolName;
    showDeleteModal.value = !showDeleteModal.value;

}
const handleDeletePool = async () => {
    //TODO : form validation 
    isLoading.value = true;
    await $client.pool.deletePool.mutate({id :poolInfo.value.id});
    poolInfo.value.id = '';
    poolInfo.value.name = '';
    await fetchPools();
    isLoading.value = false;
    showDeleteModal.value = false;
}
